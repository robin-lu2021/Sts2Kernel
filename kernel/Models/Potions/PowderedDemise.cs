public sealed class PowderedDemise : global::MegaCrit.Sts2.Core.PotionModel
{
	private const string _demiseKey = "Demise";

	public override PotionRarity Rarity => PotionRarity.Uncommon;

	public override PotionUsage Usage => PotionUsage.CombatOnly;

	public override TargetType TargetType => TargetType.AnyEnemy;

	protected override IEnumerable<DynamicVar> CanonicalVars => new global::_003C_003Ez__ReadOnlySingleElementList<DynamicVar>(new DynamicVar("Demise", 9m));

	protected override void OnUse(PlayerChoiceContext? choiceContext, Creature? target)
	{
		global::MegaCrit.Sts2.Core.PotionModel.AssertValidForTargetedPotion(target);
		PowerCmd.Apply<DemisePower>(target, base.DynamicVars["Demise"].BaseValue, base.Owner.Creature, null);
	}
}
